cadena = "Te quiero solo como amigo"
resultado_1 = cadena[0:2]
print("Los dos caracteres son: ", resultado_1)
resultado_2 = cadena[22:25]
print("Los tres caracteres son: ", resultado_2)
resultado_3 = cadena[0::2]
print("La cadena cada dos caracteres es: ", resultado_3)
cadena_invertida = cadena[::-1]
print("La cadena invertida es: ", cadena_invertida)
print("La cadena en un sentido y en sentido inverso es: ", cadena + cadena_invertida)